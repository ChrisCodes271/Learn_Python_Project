import math #import the math function

pi = 3.141592653859
x = 1
y = 2
z = 3

print(round(pi)) #will round to nearest digit
print(math.ceil(pi)) #will round up!
print(math.floor(pi)) #will round down
print(abs(pi)) #print absolute value
print(pow(pi,2)) #does a power. Requires base , exponent
print(math.sqrt(pi)) #does sqrt
print(max(x,y,z)) #finds max of passed values
print(min(x,y,z)) #finds min
